"""
ASR 音频合并服务文件大小配置

统一管理所有与文件大小相关的配置项，包括：
- 文件最小大小限制
- 文件大小阈值
- 各种文件类型的大小要求
"""


class AsrSizeConfig:
    """
    ASR 文件大小配置类

    所有大小单位均为字节（除非特别注明）
    """

    # ==================== 文件最小大小配置 ====================

    # 音频文件最小大小：用于验证音频文件是否有效
    # 默认 100 字节（避免空文件或损坏文件）
    AUDIO_FILE_MIN_SIZE: int = 100

    # 合并输出文件最小大小：用于验证合并后的音频文件和检查点文件（merged.wav）
    # 默认 1024 字节（1KB），确保文件不是空文件或损坏文件
    MERGED_OUTPUT_MIN_SIZE: int = 100

    # 可选文件最小大小：用于笔记文件、流式识别文件、元数据文件等可选文件
    # 默认 0，允许空文件（这些文件可能不存在或为空）
    OPTIONAL_FILE_MIN_SIZE: int = 0

    # ==================== 文件大小阈值配置（用于 Diff 服务）====================

    # 小文件阈值：小于此大小的文件将全量注入
    # 默认 50KB
    SMALL_FILE_THRESHOLD: int = 50 * 1024

    # 中等文件阈值：大于此大小的文件将使用预览模式
    # 默认 200KB
    MEDIUM_FILE_THRESHOLD: int = 200 * 1024

    # ==================== 行数阈值配置（辅助判断，与大小阈值配合使用）====================

    # 小文件行数阈值：小于此行数的文件视为小文件
    # 默认 500 行
    SMALL_FILE_LINES: int = 500

    # 中等文件行数阈值：大于此行数的文件视为大文件
    # 默认 2000 行
    MEDIUM_FILE_LINES: int = 2000


# 全局配置实例
asr_size_config = AsrSizeConfig()
