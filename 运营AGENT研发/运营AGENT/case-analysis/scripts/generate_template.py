def print_usage():
    """打印用法说明"""
    print(__doc__)
    sys.exit(1)


def main():
    if len(sys.argv) < 3:
        print_usage()

    case_type = sys.argv[1].lower()
    name = sys.argv[2]
    date = datetime.now().strftime("%Y-%m-%d")

    if case_type == "activity":
        template = ACTIVITY_TEMPLATE
        filename = f"activity_{name.replace(' ', '_')}.md"
    elif case_type == "content":
        template = CONTENT_TEMPLATE
        filename = f"content_{name.replace(' ', '_')}.md"
    else:
        print("错误：案例类型必须是 'activity' 或 'content'")
        print_usage()

    # 生成模板文件
    output = template.format(name=name, date=date)

    # 输出到当前目录或指定目录
    if len(sys.argv) > 3:
        output_dir = sys.argv[3]
        os.makedirs(output_dir, exist_ok=True)
        filepath = os.path.join(output_dir, filename)
    else:
        filepath = filename

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(output)

    print(f"✅ 案例拆解模板已生成: {filepath}")
    print(f"   类型: {case_type}")
    print(f"   名称: {name}")


if __name__ == "__main__":
    main()
